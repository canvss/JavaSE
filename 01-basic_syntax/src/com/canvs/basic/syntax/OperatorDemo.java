package com.canvs.basic.syntax;

public class OperatorDemo {
    public static void main(String [] args){
        int a = 3;
        int b = 4;
        System.out.println(a + b);
        System.out.println(b / a);
        System.out.println(a - b);
        System.out.println(b % a);
        System.out.println(a * b);
        System.out.println(++ a);
        System.out.println(-- b);
    }
}
