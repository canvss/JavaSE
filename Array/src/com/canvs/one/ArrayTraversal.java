public class ArrayTraversal {
    public static void main(String[] args) {
        String [] foods = new String[]{"麻婆豆腐","宫保鸡丁","青椒肉丝"};
        for (int i = 0; i < foods.length; i++) {
            System.out.println(foods[i]);
        }
    }
}
